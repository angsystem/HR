val keystoreProperties = Properties().apply {
    if (keystorePropertiesFile.exists()) {
        keystorePropertiesFile.inputStream().use(::load)
    }
}

android {
    namespace = "com.angsystem.hr"
    compileSdk = 35

    defaultConfig {
        applicationId = "com.angsystem.hr"
        minSdk = 24
